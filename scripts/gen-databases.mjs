// Generates lib/databases/generated.ts from scripts/dbms-databases.json.
//
// The JSON is the single source of truth for a paper's databases: the DDL and
// seed the questions actually run against, and the table metadata the schema
// reference renders. Only the metadata is generated into TypeScript, because
// only the metadata needs to reach the browser — the SQL is baked into each
// question's setup_sql by the importer and never shipped as a module.
//
//   node scripts/gen-databases.mjs
//
// Run scripts/check-databases.mjs afterwards: it executes the DDL in PGlite and
// asserts the generated metadata matches the tables Postgres actually created.
import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const root = join(here, "..");
const { databases } = JSON.parse(
  readFileSync(join(here, "dbms-databases.json"), "utf8"),
);

const s = (v) => JSON.stringify(v);

const body = databases
  .map((db) => {
    const tables = db.tables
      .map((t) => {
        const cols = t.columns
          .map(
            (c) =>
              `      { name: ${s(c.name)}, type: ${s(c.type)}, pk: ${c.pk}, fk: ${s(c.fk ?? null)} },`,
          )
          .join("\n");
        return `  {\n    name: ${s(t.name)},\n    columns: [\n${cols}\n    ],\n  },`;
      })
      .join("\n");
    return `export const ${db.key}: SchemaDatabase = {
  key: ${s(db.key)},
  label: ${s(db.label)},
  blurb: ${s(db.blurb)},
  tables: [
${tables}
  ],
};`;
  })
  .join("\n\n");

const out = `// GENERATED by scripts/gen-databases.mjs from scripts/dbms-databases.json.
// Do not edit by hand — edit the JSON and re-run the generator.
//
// Table metadata only. The DDL and seed data that create these databases live
// in the JSON and are baked into each question's setup_sql at import time.

import type { SchemaTable } from "@/lib/databases";

export interface SchemaDatabase {
  key: string;
  label: string;
  blurb: string;
  tables: SchemaTable[];
}

${body}

export const DATABASES: Record<string, SchemaDatabase> = {
${databases.map((d) => `  ${d.key},`).join("\n")}
};

/** Look up a database by the \`db:<key>\` tag carried on a paper's questions. */
export function schemaFor(key: string | null | undefined): SchemaDatabase | null {
  if (!key) return null;
  return DATABASES[key] ?? null;
}
`;

mkdirSync(join(root, "lib", "databases"), { recursive: true });
writeFileSync(join(root, "lib", "databases", "generated.ts"), out);
console.log(
  `Generated lib/databases/generated.ts — ${databases.length} databases, ` +
    `${databases.reduce((n, d) => n + d.tables.length, 0)} tables.`,
);
